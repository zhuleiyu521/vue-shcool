<template>
    <label class="switch" :class="{ 'is-disabled': disabled,}">
        <input class="switch_input" type="checkbox" v-model="_value" :name="name" :disabled="disabled">
        <span class="switch_core" ref="core">
            <span ref="switchbutton" class="switch_button" :style="buttonStyle"></span>
        </span>
    </label>
</template>
<script>
export default {
  props: {
    value: {
      //v-model
      type: Boolean,
      default: true
    },
    disabled: {
      // 是否禁用
      type: Boolean,
      default: false
    },
    onColor: {
      //switch 打开时的背景色
      type: String,
      default: ""
    },
    name: {
      //switch 对应的 name 属性
      type: String,
      default: ""
    }
  },
  data() {
    return {
      buttonStyle: {
        transform: ""
      }
    };
  },
  computed: {
    _value: {
      get() {
        return this.value;
      },
      set(val) {
        this.$emit("input", val);
      }
    }
  },
  watch: {
    value() {
      this.setBackgroundColor();
      this.handleButtonTransform();
    }
  },
  methods: {
    handleButtonTransform() {
      this.buttonStyle.transform = this.value
        ? `translate(12px, -1px)`
        : "translate(0, -1px)";
    },
    setBackgroundColor() {
      if (this.value) {
        this.$refs.core.style.borderColor = "#0cbb6e";
        this.$refs.core.style.backgroundColor = "#FFFFFF";
        this.$refs.switchbutton.style.backgroundColor = "#009d74";
      } else {
        this.$refs.core.style.borderColor = "#c8c8c8";
        this.$refs.core.style.backgroundColor = "#FFFFFF";
        this.$refs.switchbutton.style.backgroundColor = "#009d74";
      }
    }
  },
  mounted() {
    this.handleButtonTransform();
    if (this.onColor && !this.disabled) {
      this.setBackgroundColor();
    }
  }
};
</script>
